package ifTest;
// 10 번 : if~else문
public class IfTest02 {//클래스 시작
	public static void main(String[] args) { //main시작
		
		System.out.println("출력 시작");
		
		if(true) {//if 문 영역
			
		System.out.println("조건식이 true입니다");
		}else{
			//else문 영역
			System.out.println("조건식이 false입니다");
		}
		
		System.out.println("출력 끝");
		
	} //main종료

}//클래스 종료
